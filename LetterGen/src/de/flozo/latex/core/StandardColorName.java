package de.flozo.latex.core;

public enum StandardColorName {

    NONE,
    BLACK,
    BLUE,
    BROWN,
    CYAN,
    DARKGRAY,
    GRAY,
    GREEN,
    LIGHTGRAY,
    LIME,
    MAGENTA,
    OLIVE,
    ORANGE,
    PINK,
    PURPLE,
    RED,
    TEAL,
    VIOLET,
    WHITE,
    YELLOW;


    public String getString() {
        return name().toLowerCase();
    }
}
